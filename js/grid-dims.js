const COLS = ['A','B','C','D','E','F','G','H','I','J']

function build(raw) {
  const cols = COLS.map(c => raw.colW[c] ?? DEFAULT_COL)
  const rows = []
  for (let r = 1; r <= raw.maxRow; r++) rows[r] = raw.rowH[r] ?? DEFAULT_ROW
  return { cols, rows, maxRow: raw.maxRow }
}

export const GRID = Object.fromEntries(
  Object.entries(RAW).map(([k, v]) => [k, build(v)])
)
